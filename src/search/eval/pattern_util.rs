#[cfg(not(target_feature="bmi2"))]
pub fn pdep64(a: u64, mask: u64) -> u64 {
    let mut temp = mask;
    let mut dst = 0;
    let mut idx = 1u64;
    
    while temp != 0 {
        if a & idx != 0 {
            dst |= temp & temp.wrapping_neg();
        }
        temp &= temp - 1;
        idx = idx.wrapping_add(idx);
    }

    dst
}

#[cfg(all(target_arch="x86_64", target_feature="bmi2"))]
pub fn pext64(a: u64, mask: u64) -> u64 {
    unsafe {
        _pext_u64(a, mask)
    }
}

#[cfg(all(target_arch="x86", target_feature="bmi2"))]
pub fn pext64(a: u64, mask: u64) -> u64 {
    let mask_low = mask & 0xFF_FF_FF_FF;
    let low = unsafe {
        _pext_u32((a & 0xFF_FF_FF_FF) as u32, mask_low as u32)
    };

    let high = unsafe {
        _pext_u32(a >> 32, mask >> 32)
    };

    low | (high << mask_low.count_ones())
}

#[cfg(not(target_feature="bmi2"))]
pub fn pext64(a: u64, mask: u64) -> u64 {
    let mut temp = mask;
    let mut dst = 0;
    let mut idx = 1u64;
    
    while temp != 0 {
        if a & temp & temp.wrapping_neg() != 0 {
            dst |= idx;
        }
        idx = idx.wrapping_add(idx);
        temp &= temp - 1;
    }

    dst
}

#[cfg(target_arch="x86_64")]
pub fn flip_vertical(a: u64) -> u64 {
    unsafe {
        _bswap64(a as i64) as u64
    }
}

#[cfg(not(target_arch="x86_64"))]
pub fn flip_vertical(a: u64) -> u64 {
    const K1: u64 = 0x00FF00FF00FF00FF;
    const K2: u64 = 0x0000FFFF0000FFFF;
    
    let mut x = 0;
    x = ((a >>  8) & K1) | ((a & K1) <<  8);
    x = ((a >> 16) & K2) | ((a & K2) << 16);
    x = ( a >> 32)       | ( a       << 32);
    
    x
}

pub fn flip_diag(a: u64) -> u64 {
    const K1: u64 = 0x5500550055005500;
    const K2: u64 = 0x3333000033330000;
    const K4: u64 = 0x0f0f0f0f00000000;
    
    let mut t;
    let mut x = a;
    
    t  = K4 & (x ^ (x << 28));
    x ^=       t ^ (t >> 28) ;
    t  = K2 & (x ^ (x << 14));
    x ^=       t ^ (t >> 14) ;
    t  = K1 & (x ^ (x <<  7));
    x ^=       t ^ (t >>  7) ;

    x
}